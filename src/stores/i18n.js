function valueByPath(obj, path) {
  return String(path || '').split('.').reduce((acc, key) => acc && acc[key] !== undefined ? acc[key] : undefined, obj)
}

export const useI18nStore = defineStore('i18n', () => {
  const language = ref('ru')
  const availableLanguages = [
    { value: 'ru', label: 'Русский' },
    { value: 'en', label: 'English' }
  ]

  const currentLanguageLabel = computed(() => availableLanguages.find(i => i.value === language.value)?.label || 'Русский')

  function t(key, params = {}) {
    const value = valueByPath(messages[language.value], key) ?? valueByPath(messages.en, key) ?? key
    return String(value).replace(/\{(\w+)\}/g, (_, name) => params[name] ?? '')
  }

  async function loadLanguage() {
    try {
      const cfg = await window.electron.loadConfig()
      language.value = ['ru', 'en'].includes(cfg.language) ? cfg.language : 'ru'
    } catch {
      language.value = 'ru'
    }
  }

  async function setLanguage(next) {
    language.value = ['ru', 'en'].includes(next) ? next : 'ru'
    try {
      const cfg = await window.electron.loadConfig()
      await window.electron.saveConfig({ ...cfg, language: language.value })
    } catch {}
  }

  return { language, availableLanguages, currentLanguageLabel, t, loadLanguage, setLanguage }
})
